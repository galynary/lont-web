import { Injectable } from '@angular/core';

@Injectable()
export class SampleService {
  pageNumber: number;
  pageSize: number;
  constructor() {
    this.pageNumber = 0;
    this.pageSize = 20;
  }

  call(){
    console.log("called for page "+this.pageNumber+" with pageSize "+this.pageSize);
    this.pageNumber++;
  }
}
